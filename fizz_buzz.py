print('Welcome to FizzBuzz')
input('Please press enter to start a chain and show the FizzBuzz principle')
for fizzbuzz in range(21):
    if fizzbuzz % 3 == 0 and fizzbuzz % 5 == 0:
        print("fizzbuzz")
        continue
    elif fizzbuzz % 3 == 0:
        print("fizz")
        continue
    elif fizzbuzz % 5 == 0:
        print("buzz")
        continue
    print(fizzbuzz)